            <li>Loyang loaf 26 x 11 x 8 cm</li>
            <li>Piring</li>
        </ul>
        <h5>Cara membuat:</h5>
        <ol>
            <li>Campur tepung terigu, baking powder, dan garam. Aduk rata. Sisihkan.</li>
            <li>Kocok margarin, gula pasir, dan cake softener menggunakan mixer hingga mengembang.</li>
            <li>Masukkan telur satu per satu sambil dikocok hingga mengembang.</li>
            <li>Tambahkan campuran tepung terigu, pisang, dan susu tawar cair ke dalam adonan telur. Aduk rata.</li>
            <li>Siapkan loyang loaf 26 x 11 x 8 cm yang telah dialas kertas roti dan dioles margarin. Tuang adonan, ratakan.</li>
            <li>Taburi keju cheddar parut di atasnya. Panggang adonan di dalam oven bertemperatur 180 Â°C selama 45 menit 
