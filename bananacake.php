        <h5>Bahan - bahan:</h5>
        <ol>
            <li>Tepung terigu protein sedang 300 gram</li>
            <li>Pisang ambon (haluskan) 250 gram</li>
            <li>Margarin 250 gram</li>
            <li>Gula pasir 250 gram</li>
            <li>Telur 6 butir</li>
            <li>Susu tawar cair 125 ml</li>
            <li><i>Cake softener</i> 1 sdt</li>
            <li><i>Baking powder</i> 1 sdm</li>
            <li>Garam halus 1/2 sdt</li>
            <li>Keju cheddar (parut) 60 gram</li>
