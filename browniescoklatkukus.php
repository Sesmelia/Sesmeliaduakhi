        <p>Brownies merupakan salah satu makanan yang memiliki banyak peminatnya. 
            Dengan warna coklat yang pekat dan rasa manis bercampur sedikit rasa coklat, kue ini sangat digemari. 
            Brownies selain nikmat juga mudah untuk dibuat. Tidak heran kue coklat ini disukai oleh pembuat kue pemula. 
            Jika kita mundur kembali ke zaman dulu, brownies biasanya dipanggang. 
            Namun, sekarang mulai banyak bermunculan brownies yang tidak dipanggang untuk memasaknya. 
            Caranya? Tentu saja dikukus, jadi tidak ada lagi brownies panggang, melainkan brownies kukus.
        </p>
        <h5>Bahan - bahan:</h5>
        <ol>
            <li>Telur antero 4 butir</li>
            <li>Gula pasir 250 gram</li>
            <li>SP/TBM/Ovalet 1/4 sdm</li>
            <li>Susu kental manis coklat 100 ml</li>
            <li>Air 50 ml</li>
            <li>Tepung terigu 100 gram</li>
            <li>Coklat bubuk 50 gram</li>
            <li>Minyak sayur 130 ml</li>
        </ol>
        <h5>Alat - alat:</h5>
        <ul>
            <li>Kukusan</li>
            <li>Mixer</li>
            <li>Baskom</li>
            <li>Sendok</li>
            <li>Solet</li>
            <li>Loyang loaf persegi</li>
            <li>Piring</li>
        </ul>
        <h5>Cara membuat:</h5>
        <ol>
            <li>Campur susu dan air, kemudian aduk rata dan sisihkan.</li>
            <li>Mixer telur, sp, dan gula sampai mengembang (memutih).</li>
            <li>Masukkan nomer 1, kemudian aduk kembali.</li>
            <li>Masukkan tepung terigu dan coklat bubuk, kemudian aduk rata.</li>
            <li>Tuangkan minyak sayur, kemudian aduk menggunakan solet dengan teknik melipat.</li>
            <li>Tuang adonan ke loyang loaf persegi panjang yang telah dialasi kertas roti, kukus selama 10 menit. 
                Kemudian tuangkan sisa adonan dan kukus selama 20 menit.</li>
            <li>Angkat dan dinginkan.</li>
            <li>Potong brownies sesuai selera dan brownies siap dihidangkan.</li>
        </ol>
